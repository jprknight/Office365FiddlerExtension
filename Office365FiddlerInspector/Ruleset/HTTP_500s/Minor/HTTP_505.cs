using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Office365FiddlerInspector.Services;
using Fiddler;

namespace Office365FiddlerInspector.Ruleset
{
    class HTTP_505
    {
        internal Session session { get; set; }
        public void HTTP_505_HTTP_Version_Not_Supported(Session session)
        {
            session["X-ResponseAlert"] = "HTTP 505 HTTP Version Not Supported.";
            session["X-ResponseComments"] = Preferences.GetStrNoKnownIssue();

            FiddlerApplication.Log.LogString("Office365FiddlerExtension: " + session.id + " HTTP 505 HTTP Version Not Supported.");

            session["X-ResponseCodeDescription"] = "505 HTTP Version Not Supported";

            // Nothing meaningful here, let further processing try to pick up something.
            SessionProcessor.Instance.SetSACL(this.session, "0");
            SessionProcessor.Instance.SetSTCL(this.session, "0");
            SessionProcessor.Instance.SetSRSCL(this.session, "0");
        }
    }
}
