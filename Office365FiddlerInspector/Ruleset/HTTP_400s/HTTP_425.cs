using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Office365FiddlerInspector.Services;
using Fiddler;

namespace Office365FiddlerInspector.Ruleset
{
    class HTTP_425 : ActivationService
    {

        public void HTTP_425_Too_Early(Session session)
        {
            session["X-ResponseAlert"] = "HTTP 425 Too Early (RFC 8470).";
            session["X-ResponseComments"] = Preferences.GetStrNoKnownIssue();

            FiddlerApplication.Log.LogString("Office365FiddlerExtension: " + session.id + " HTTP 425 Too Early (RFC 8470).");

            session["X-ResponseCodeDescription"] = "425 Too Early (RFC 8470)";

            // Nothing meaningful here, let further processing try to pick up something.
            Preferences.SetSACL(session, "0");
            Preferences.SetSTCL(session, "0");
            Preferences.SetSRSCL(session, "0");
        }
    }
}
