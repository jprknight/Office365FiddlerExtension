using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Office365FiddlerInspector.Services;
using Fiddler;

namespace Office365FiddlerInspector.Ruleset
{
    class HTTP_203 : ActivationService
    {

        public void HTTP_203_NonAuthoritive_Answer(Session session)
        {
            session["X-ResponseAlert"] = "203 Non-Authoritative Information";
            session["X-ResponseComments"] = Preferences.GetStrNoKnownIssue();

            FiddlerApplication.Log.LogString("Office365FiddlerExtension: " + session.id + " HTTP 203 Non-Authoritative Information.");

            session["X-ResponseCodeDescription"] = "203 Non-Authoritative Information";

            // Nothing meaningful here, let further processing try to pick up something.
            Preferences.SetSACL(session, "0");
            Preferences.SetSTCL(session, "0");
            Preferences.SetSRSCL(session, "0");
        }
    }
}
